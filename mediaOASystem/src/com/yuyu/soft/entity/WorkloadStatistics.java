package com.yuyu.soft.entity;

import java.math.BigDecimal;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.yuyu.soft.entity.common.CommonEntity;

/**
 * 工作量统计表                      
 * @Filename: WorkloadStatistics.java
 * @Version: 1.0
 * @Author: 李明
 * @Email: limn_xmj@163.com
 *
 */
@Entity
@Table(name = "tb_workload_statistics")
public class WorkloadStatistics extends CommonEntity {

    private static final long serialVersionUID = 1L;

    private Date              log_date;             //记入时间
    private String            name;                 //姓名
    @Column(precision = 6, scale = 1)
    private BigDecimal        bright_spot;          //亮点
    @Column(precision = 6, scale = 1)
    private BigDecimal        app_write;            //APP写
    @Column(precision = 6, scale = 1)
    private BigDecimal        app_send;             //APP发
    @Column(precision = 6, scale = 1)
    private BigDecimal        fb_write;             //FB写
    @Column(precision = 6, scale = 1)
    private BigDecimal        fb_send;              //FB发
    @Column(precision = 6, scale = 1)
    private BigDecimal        weibo_write;          //微博写
    @Column(precision = 6, scale = 1)
    private BigDecimal        weibo_send;           //微博发
    @Column(precision = 6, scale = 1)
    private BigDecimal        twt_write;            //TWT写
    @Column(precision = 6, scale = 1)
    private BigDecimal        twt_send;             //TWT发
    @Column(precision = 6, scale = 1)
    private BigDecimal        upload_yt_or_tencent; //上传YT+腾讯
    @Column(precision = 6, scale = 1)
    private BigDecimal        tumblr_send;          //tumblr发
    @Column(precision = 6, scale = 1)
    private BigDecimal        WeChat;               //微信
    @Column(precision = 6, scale = 1)
    private BigDecimal        inst;                 //inst
    @Column(precision = 6, scale = 1)
    private BigDecimal        google;               //Google+
    @Column(precision = 6, scale = 1)
    private BigDecimal        pinterest;            //pinterest
    @Column(precision = 7, scale = 1)
    private BigDecimal        total;                //小结
    private String            others;               //其他
    private String            remark;               //备忘

    public Date getLog_date() {
        return log_date;
    }

    public void setLog_date(Date log_date) {
        this.log_date = log_date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public BigDecimal getBright_spot() {
        return bright_spot;
    }

    public void setBright_spot(BigDecimal bright_spot) {
        this.bright_spot = bright_spot;
    }

    public BigDecimal getApp_write() {
        return app_write;
    }

    public void setApp_write(BigDecimal app_write) {
        this.app_write = app_write;
    }

    public BigDecimal getApp_send() {
        return app_send;
    }

    public void setApp_send(BigDecimal app_send) {
        this.app_send = app_send;
    }

    public BigDecimal getFb_write() {
        return fb_write;
    }

    public void setFb_write(BigDecimal fb_write) {
        this.fb_write = fb_write;
    }

    public BigDecimal getFb_send() {
        return fb_send;
    }

    public void setFb_send(BigDecimal fb_send) {
        this.fb_send = fb_send;
    }

    public BigDecimal getWeibo_write() {
        return weibo_write;
    }

    public void setWeibo_write(BigDecimal weibo_write) {
        this.weibo_write = weibo_write;
    }

    public BigDecimal getWeibo_send() {
        return weibo_send;
    }

    public void setWeibo_send(BigDecimal weibo_send) {
        this.weibo_send = weibo_send;
    }

    public BigDecimal getTwt_write() {
        return twt_write;
    }

    public void setTwt_write(BigDecimal twt_write) {
        this.twt_write = twt_write;
    }

    public BigDecimal getTwt_send() {
        return twt_send;
    }

    public void setTwt_send(BigDecimal twt_send) {
        this.twt_send = twt_send;
    }

    public BigDecimal getUpload_yt_or_tencent() {
        return upload_yt_or_tencent;
    }

    public void setUpload_yt_or_tencent(BigDecimal upload_yt_or_tencent) {
        this.upload_yt_or_tencent = upload_yt_or_tencent;
    }

    public BigDecimal getTumblr_send() {
        return tumblr_send;
    }

    public void setTumblr_send(BigDecimal tumblr_send) {
        this.tumblr_send = tumblr_send;
    }

    public BigDecimal getWeChat() {
        return WeChat;
    }

    public void setWeChat(BigDecimal weChat) {
        WeChat = weChat;
    }

    public BigDecimal getInst() {
        return inst;
    }

    public void setInst(BigDecimal inst) {
        this.inst = inst;
    }

    public BigDecimal getGoogle() {
        return google;
    }

    public void setGoogle(BigDecimal google) {
        this.google = google;
    }

    public BigDecimal getPinterest() {
        return pinterest;
    }

    public void setPinterest(BigDecimal pinterest) {
        this.pinterest = pinterest;
    }

    public BigDecimal getTotal() {
        return total;
    }

    public void setTotal(BigDecimal total) {
        this.total = total;
    }

    public String getOthers() {
        return others;
    }

    public void setOthers(String others) {
        this.others = others;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
